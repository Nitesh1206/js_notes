// while loop

let score = 0

while (score <= 10) {
    console.log(score);
    score = score + 1
}

const heros = ["Doctor Strange", "Spider Man", "Black Panther"]
let i = 0
while (i < heros.length) {
    console.log(heros[i]);
    i += 1
}


// do while loop

let num = 6

do {
    console.log(num);
    num++
} while (num <= 5)

